@app.route('/register', methods=['GET', 'POST'])
def register():
    user = RegisterForm()
    if user.validate_on_submit():

        if User.query.filter_by(email=user.email.data).first():
            flash("You've already signed up with that email, log in instead!")
            return redirect(url_for('login'))
        hash_password = generate_password_hash(user.password.data, method='pbkdf2:sha256', salt_length=8)
        new_user = User(
            email=user.email.data,
            name=user.user.data,
            password=hash_password
        )
        db.session.add(new_user)
        db.session.commit()
        login_user(new_user)
        return redirect(url_for("get_all_posts"))

    return render_template("register.html", form=user)


@app.route('/login', methods=['GET', 'POST'])
def login():
    user_login = LoginForm()
    if user_login.validate_on_submit():
        email = user_login.email.data
        password_input = user_login.password.data
        user = User.query.filter_by(email=email).first()
        if user is not None and check_password_hash(user.password, password_input):
            login_user(user)
            # print(current_user.id)
            return redirect(url_for('get_all_posts'))
        else:
            flash('either email or password is incorrect please pu correct')
    return render_template("login.html", form=user_login)


